#include "dot.hpp"

//===============================================================================================================================================

#include <cmath>
#include <iostream>

//===============================================================================================================================================

namespace Geometry
{
    Dot::Dot(double x_coord, double y_coord) :
        x(x_coord),
        y(y_coord)
    {}

    bool Dot::is_valid ()
    {
        return !(std::isnan(x) || std::isinf(x) || std::isnan(y) || std::isinf(y));
    }

    void Dot::print ()
    {
        std::cout << "x: " << x << " y: " << y << std::endl;
    }
}

