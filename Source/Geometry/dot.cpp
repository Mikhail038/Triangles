#include "dot.hpp"

//===============================================================================================================================================

#include <cmath>
#include <iostream>

//===============================================================================================================================================

namespace Geometry
{
    Dot::Dot(double x_coord, double y_coord, double z_coord) :
        x_(x_coord),
        y_(y_coord),
        z_(z_coord)
    {}

    bool Dot::is_valid ()
    {
        return !(std::isnan(x_) || std::isinf(x_) || std::isnan(y_) || std::isinf(y_) || std::isinf(z_) || std::isinf(z_));
    }

    void Dot::print ()
    {
        std::cout << "x: " << x_ << " y: " << y_ << " z: " << z_ << std::endl;
    }
}

